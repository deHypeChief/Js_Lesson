// Variables
// Variables are named containers used to store data values in a program.
// Declared using keywords like var, let, or const (ES6+).

// Variables must follow naming rules: 
// >> start with a letter, underscore, or dollar sign; 
// >> can include letters, numbers, underscores, and dollar signs.


// Examples:
var age = 25; // this is dynamic
let name = "John"; // this is dynamic also
const PI = 3.14; // this can't be changed


// Data Types:
// JavaScript has various data types:


// Primitive Types:
// >> Number: Represents both integers and floating-point numbers.
// >> String: Represents text and is enclosed in single or double quotes.
// >> Boolean: Represents true or false values.
// >> Null: Represents the absence of a value (intentionally assigned).
// >> Undefined: Represents the absence of a value (default value for uninitialized variables).
// >> Symbol: Introduced in ES6, represents a unique and immutable value.

// Complex Types:
// >> Object: Represents a collection of key-value pairs (properties and methods).
// >> Array: Represents an ordered list of values.
// >> Function: A reusable block of code that can be invoked by its name.
